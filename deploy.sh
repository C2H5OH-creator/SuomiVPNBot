    echo "ADMINS=[–≤–∞—à_id_–∞–¥–º–∏–Ω–∞]"
    echo ""
    echo "–ü—Ä–∏–º–µ—Ä:"
    echo "TELEGRAM_BOT_API_TOKEN=1234567890:ABCdefGHIjklMNOpqrsTUVwxyz"
